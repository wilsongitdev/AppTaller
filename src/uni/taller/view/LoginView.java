    public class ClaseTime implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent ae) {
            a = jProgressBar1.getValue();
            if (a < 100) {
                a++;
                jProgressBar1.setValue(a);
            } else {
                objTimer.stop();
                Cerrar();
                Principalview f = new Principalview();
                f.show();
            }
        }
    }

    private void Cerrar() {
        dispose();
    }

